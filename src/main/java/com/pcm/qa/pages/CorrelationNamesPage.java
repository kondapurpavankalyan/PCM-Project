	public void selectcorrelation(String name) {
		
		List<WebElement> li = driver.findElements(By.xpath("//div//mat-option"));
		
		for(int i=0;i<=li.size();i++) {
			
			String text = li.get(i).getText();
			
			if(text.contentEquals(name)) {
				
				li.get(i).click();
				break;
			}
			
		}
		
	}



	
